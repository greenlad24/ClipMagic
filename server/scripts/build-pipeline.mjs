// Bundles the original AI pipeline (repo src/api/runPipeline.ts) into a single
// ESM file the server can import, aliasing the OpenAI SDK to our Groq+Claude
// shim and the Zite backend SDK to our SQLite store. Native better-sqlite3 is
// left external (can't be bundled).
import { build } from "esbuild";
import path from "node:path";
import fs from "node:fs";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const serverRoot = path.resolve(__dirname, "..");
const repoRoot = path.resolve(serverRoot, "..");

// Our server source uses NodeNext-style ".js" specifiers that actually point at
// ".ts" files. esbuild doesn't rewrite those, so resolve them ourselves.
const jsToTsPlugin = {
  name: "js-to-ts",
  setup(b) {
    b.onResolve({ filter: /\.js$/ }, (args) => {
      if (args.kind === "entry-point" || !args.importer) return;
      if (!args.path.startsWith(".")) return;
      const abs = path.resolve(args.resolveDir, args.path);
      const tsPath = abs.replace(/\.js$/, ".ts");
      if (fs.existsSync(tsPath)) return { path: tsPath };
      return; // fall back to esbuild's default resolution
    });
  },
};

await build({
  entryPoints: [path.join(serverRoot, "src/ai/pipeline-entry.ts")],
  bundle: true,
  platform: "node",
  format: "esm",
  target: "node20",
  outfile: path.join(serverRoot, "dist/ai/pipeline-bundle.js"),
  external: ["better-sqlite3"],
  plugins: [jsToTsPlugin],
  alias: {
    openai: path.join(serverRoot, "src/ai/openai-shim.ts"),
    "zite-integrations-backend-sdk": path.join(serverRoot, "src/zite/sdk.ts"),
    "zite-file-upload-sdk": path.join(serverRoot, "src/ai/upload-shim.ts"),
  },
  logLevel: "info",
  banner: { js: "// AUTO-GENERATED by scripts/build-pipeline.mjs — do not edit." },
  absWorkingDir: repoRoot,
});

console.log("[build-pipeline] wrote dist/ai/pipeline-bundle.js");
